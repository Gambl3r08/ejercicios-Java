import java.util.*;

public class autobox{
    public static void main(String[] args){
        System.out.println("Inicio");
        Integer a = 127;
        Integer b = 127;
        Integer c = 128;
        Integer d = 128;
        
        System.out.println(a == b);
        System.out.println(c <= d);
        System.out.println(c >= d);
        System.out.println(c == d);
        
    }

}